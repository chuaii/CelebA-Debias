# 2. 模型推論與數據收集
# ==========================================
import torch
import pandas as pd
from tqdm import tqdm

device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
print(f"💻 目前使用的運算設備: {device}")

# 定義要評估的模型清單 (請確認檔名與你雲端硬碟中的一致)
# 這裡我們取 Best Worst-Group Acc 的權重來畫圖比較
models_to_evaluate = {
    'Baseline': 'best_wg_model_Baseline.pth',
    'Oversampling': 'best_wg_model_Oversampling.pth',
    'Undersampling': 'best_wg_model_Undersampling.pth',
    'Reweighting': 'best_wg_model_Reweighting.pth',
    'Focal Loss': 'best_wg_model_Focal Loss.pth'
}

results = []

for method_name, pth_name in models_to_evaluate.items():
    if not os.path.exists(pth_name):
        print(f"⚠️ 找不到檔案: {pth_name}，跳過此模型。")
        continue

    print(f"\n🔍 正在評估: {method_name}...")

    # 初始化模型
    model = StandardResNet(num_classes=2).to(device)

    # 讀取原本的權重檔
    original_state_dict = torch.load(pth_name, map_location=device, weights_only=True)

    # 修正 key 的名稱，對齊兩邊的變數命名
    new_state_dict = {}
    for k, v in original_state_dict.items():
        # 如果當初的變數名叫 backbone，把它換成 model
        if k.startswith('backbone.'):
            new_key = k.replace('backbone.', 'model.')
        # 如果當初最後一層叫做 fc 或 classifier，對應到 model.fc
        elif k.startswith('fc.'):
            new_key = k.replace('fc.', 'model.fc.')
        elif k.startswith('classifier.'):
            new_key = k.replace('classifier.', 'model.fc.')
        else:
            new_key = k

        new_state_dict[new_key] = v

    # 載入修正後的權重 (strict=False 允許忽略一些不影響的分類器名稱差異)
    model.load_state_dict(new_state_dict, strict=False)
    model.eval()

    all_preds, all_targets, all_biases = [], [], []

    # 假設 test_loader 已經透過你的 prepare_dataloaders 準備好
    with torch.no_grad():
        for images, targets, biases in tqdm(test_loader, desc="Testing"):
            images = images.to(device)
            logits = model(images)
            preds = torch.argmax(logits, dim=1)

            all_preds.append(preds.cpu())
            all_targets.append(targets.cpu())
            all_biases.append(biases.cpu())

    all_preds = torch.cat(all_preds)
    all_targets = torch.cat(all_targets)
    all_biases = torch.cat(all_biases)

    # --- 計算整體準確率 ---
    overall_acc = (all_preds == all_targets).float().mean().item()

    # --- 計算四大群體準確率 ---
    # 假設: Target=0(非金髮), Target=1(金髮) / Bias=0(女性), Bias=1(男性)
    groups_info = {
        'Non-Blonde Female': (0, 0),
        'Non-Blonde Male':   (0, 1),
        'Blonde Female':     (1, 0),
        'Blonde Male':       (1, 1)  # 最差群體 (Worst-Group)
    }

    group_metrics = {}
    for group_name, (t, b) in groups_info.items():
        mask = (all_targets == t) & (all_biases == b)
        acc = (all_preds[mask] == all_targets[mask]).float().mean().item() if mask.sum() > 0 else 0.0
        group_metrics[group_name] = acc

    worst_group_acc = min(group_metrics.values())

    # 儲存結果
    results.append({
        'Method': method_name,
        'Overall Accuracy': overall_acc,
        'Worst-Group Accuracy': worst_group_acc,
        **group_metrics  # 展開四大群體數據
    })

df_results = pd.DataFrame(results)
print("\n📊 數據收集完成！")
display(df_results)

