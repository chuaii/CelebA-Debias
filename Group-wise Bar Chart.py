import os
import pandas as pd
from PIL import Image
import torch
from torch.utils.data import Dataset, DataLoader
import torchvision.transforms as transforms

class CelebADataset(Dataset):
    def __init__(self, root_dir, split='test', transform=None):
        self.root_dir = root_dir
        self.transform = transform

        # 處理 Kaggle 下載後可能產生的雙層資料夾結構
        img_dir = os.path.join(root_dir, 'img_align_celeba', 'img_align_celeba')
        if not os.path.exists(img_dir):
            img_dir = os.path.join(root_dir, 'img_align_celeba')

        attr_path = os.path.join(root_dir, 'list_attr_celeba.csv')
        eval_path = os.path.join(root_dir, 'list_eval_partition.csv')

        # 讀取屬性與分割檔
        attrs = pd.read_csv(attr_path)
        partitions = pd.read_csv(eval_path)

        # 合併資料並篩選測試集 (0: train, 1: val, 2: test)
        data = pd.merge(attrs, partitions, on='image_id')
        split_map = {'train': 0, 'val': 1, 'test': 2}
        data = data[data['partition'] == split_map[split]]

        self.image_ids = data['image_id'].values
        self.img_dir = img_dir

        # 將標籤與偏見屬性轉換為 0 與 1 (-1 轉為 0)
        self.targets = ((data['Blond_Hair'] == 1).astype(int)).values
        self.biases = ((data['Male'] == 1).astype(int)).values

    def __len__(self):
        return len(self.image_ids)

    def __getitem__(self, idx):
        img_name = os.path.join(self.img_dir, self.image_ids[idx])
        image = Image.open(img_name).convert('RGB')

        if self.transform:
            image = self.transform(image)

        target = self.targets[idx]
        bias = self.biases[idx]

        return image, target, bias

# 定義 ResNet 預設的影像轉換處理
transform_test = transforms.Compose([
    transforms.Resize((224, 224)),
    transforms.ToTensor(),
    transforms.Normalize(mean=[0.485, 0.456, 0.406], std=[0.229, 0.224, 0.225]),
])

# 建立 test_loader
test_dataset = CelebADataset(root_dir=dataset_path, split='test', transform=transform_test)
test_loader = DataLoader(test_dataset, batch_size=128, shuffle=False, num_workers=2)

print(f"test_loader 建立完成。測試集包含 {len(test_dataset)} 張影像。")

